export const LANGUAGE_STORAGE_KEY = "laverie-de-la-residence-language";
export type AppLanguage = "fr" | "ar";

/**
 * Traduit un libellé de poste/machine renvoyé par le backend (ex. « Poste 1 »)
 * vers l'arabe (« الغسالة 1 ») lorsque la langue active est l'arabe.
 */
export function localizeResourceLabel(label: string | undefined | null, language: AppLanguage): string {
  if (!label) {
    return "";
  }
  if (language !== "ar") {
    return label;
  }
  return label.replace(/Poste/gi, "الغسالة");
}

function getStoredLanguage(): AppLanguage | null {
  if (typeof window === "undefined") {
    return null;
  }

  const storedLanguage = window.localStorage.getItem(LANGUAGE_STORAGE_KEY);
  return storedLanguage === "fr" || storedLanguage === "ar"
    ? storedLanguage
    : null;
}

